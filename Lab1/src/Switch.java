
public class Switch {
	public static void main(String args[]) {
		
		
		
		int month = 5;
		String monthName = "Feb";

		switch (month) {
		case 1:
			monthName = "Jan";
			System.out.print(monthName);

			break;
		case 2:
			monthName = "Feb";
			System.out.print(monthName);

			break;
		case 3:
			monthName = "Mar";
			System.out.print(monthName);

			break;
		case 4:
			monthName = "Apr";
			System.out.print(monthName);

			break;
		case 5:
			monthName = "May";
			System.out.print(monthName);

			break;

		}
	
		
		System.out.print(monthName);
	}
}
